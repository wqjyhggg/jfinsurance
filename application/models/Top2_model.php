        return 1;
      }
      $base = $this->{$arr}[$idx];
			
			if ($data['isfamilyplan'] == 1) {
				$base *= 2.25;
			} else if ($data['isfamilyplan'] == 2) {
				$base *= $data['people_number'];
			}
				
			$this->premiumArr['premium'] += $base;
			
