				// 6% TAX
				$this->premiumArr['tax'] += $base * 0.06;
				$this->premiumArr['premium'] += $base * 0.06;
			}
		}
		
		if ($this->premiumArr['premium'] > 0) {
			$this->premiumArr['status'] = 'OK';
		}

		return 1;
	}
	
