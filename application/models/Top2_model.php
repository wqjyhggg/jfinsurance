			if (($data['province2'] == 'MB') || ($data['province2'] == 'ON')) {
				// 8% TAX
				$this->premiumArr['tax'] += $base * 0.08;
				$this->premiumArr['premium'] += $base * 0.08;
			} else if ($data['province2'] == 'QC') {
				// 9% TAX
				$this->premiumArr['tax'] += $base * 0.09;
				$this->premiumArr['premium'] += $base * 0.09;
