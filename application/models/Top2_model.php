	private function annual_plan($data, $ischeck) {
		if ($data['age'] < 30) {
			$idx1 = 0;
		} else if ($data['age'] < 55) {
			$idx1 = 1;
		} else if ($data['age'] < 60) {
			$idx1 = 2;
		} else if ($data['age'] < 65) {
			$idx1 = 3;
		} else if ($data['age'] < 70) {
			$idx1 = 4;
		} else if ($data['age'] < 75) {
			$idx1 = 5;
