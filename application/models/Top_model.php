	private function annual_plan($data, $ischeck) {
		if ($data['age'] < 30) {
			$idx1 = 0;
		} else if ($data['age'] < 55) {
			$idx1 = 1;
		} else if ($data['age'] < 60) {
			$idx1 = 2;
		} else if ($data['age'] < 65) {
			$idx1 = 3;
		} else if ($data['age'] < 70) {
			$idx1 = 4;
		} else if ($data['age'] < 75) {
			$idx1 = 5;
		} else {
			$this->premiumArr['message'] = 'Annual plan, age must less than 75.';
			$this->premiumArr['active_tab'] = 'date_members_tab';
			return 0;
		}
		
		if (($data['totaldays'] != 365) && ($data['totaldays'] != 366)) {
			$this->premiumArr['message'] = 'Annual plan, the days must be a year long.';
			$this->premiumArr['active_tab'] = 'date_members_tab';
			return 0;
		}
		
		if ($data['isfamilyplan']) {
			$this->premiumArr['message'] = "Annual plan isn't avaliable for family or group.";
			$this->premiumArr['active_tab'] = 'date_members_tab';
			return 0;
		}
		
		if ($ischeck) {
			return 1;
		}
		
		if ($data['annual_plan_days'] == 4) {
			$idx2 = 0;
		} else if ($data['annual_plan_days'] == 9) {
			$idx2 = 1;
		} else if ($data['annual_plan_days'] == 17) {
			$idx2 = 2;
		} else if ($data['annual_plan_days'] == 30) {
			$idx2 = 3;
		} else if ($data['annual_plan_days'] == 60) {
			$idx2 = 4;
		} else {
			$this->premiumArr['message'] = 'Unknow selected days. Please select plan days.';
			$this->premiumArr['active_tab'] = 'packages_tab';
			return 1;
		}
		
		$this->premiumArr['premium'] = $this->annual[$idx2][$idx1];
		if ($data['province2'] == 'SK') {
			// 6% TAX
			$this->premiumArr['tax'] += $this->annual[$idx2][$idx1] * 0.06;
			$this->premiumArr['premium'] += $this->annual[$idx2][$idx1] * 0.06;
		}
		
		if ($this->premiumArr['premium'] > 0) {
			$this->premiumArr['status'] = 'OK';
		}
		return 1;
	}
	
